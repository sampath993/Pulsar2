package Conditional_Statement;

public class For_Loops {

	public static void main(String[] args) {
		
		int [] myarray = {1,2,3,4,5};
		
		for (int i = 0; i < 10; i++) {
			
			System.out.println(i);
			
		}
		
		System.out.println("-----------------------");
		
		for(int index =0; index<5; index++) {
			
			System.out.println(myarray[index]);
			
		}

	}

}
