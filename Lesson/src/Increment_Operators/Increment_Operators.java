package Increment_Operators;

public class Increment_Operators {

	public static void main(String[] args) {
		//Post increment Operations
		int x= 1000;
		System.out.println(x++);
		System.out.println(x);
		//	Pre-Increment_Operation
		int y =1000;
		System.out.println(++y);
		System.out.println(y);

		//Post decrement Operations
		int k= 1000;
		System.out.println(k--);
		System.out.println(k);
		//	Pre-decrement_Operation
		int l =1000;
		System.out.println(--l);
		System.out.println(l);

	}

}
